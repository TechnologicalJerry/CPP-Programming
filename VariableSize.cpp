#include <iostream>
using namespace std;

int main() 
{    
    cout << "Size of integer: " << sizeof(int) << " Bytes" << endl;
    cout << "Size of charater: " << sizeof(char) << " Byte" << endl;
    cout << "Size of floating: " << sizeof(float) << " Bytes" << endl;
    cout << "Size of double: " << sizeof(double) << " Bytes" << endl;
    return 0;
}