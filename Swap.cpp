#include <bits/stdc++.h>
using namespace std;
int main() {
  int ss = 9;
  int sg = 14;
  cout << "Value of ss before swapping: " << ss << endl;
  cout << "Value of sg before swapping: " << sg << endl;
  swap(ss, sg);
  cout << "Value of ss after swapping: " << ss << endl;
  cout << "Value of sg after swapping: " << sg << endl;
  return 0;
}